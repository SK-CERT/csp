package com.intrasoft.csp.vcb.admin.service.exception;

public class OpenfireException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8896771344098480003L;

	public OpenfireException() {
		super();
	}

	public OpenfireException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OpenfireException(String message, Throwable cause) {
		super(message, cause);
	}

	public OpenfireException(String message) {
		super(message);
	}

	public OpenfireException(Throwable cause) {
		super(cause);
	}

}
